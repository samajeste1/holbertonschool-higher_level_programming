#!/usr/bin/env python3
"""
Module for string concatenation with type annotations.
Provides a function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and return the result.

    Args:
        str1: First string
        str2: Second string

    Returns:
        The concatenation of str1 and str2
    """
    return str1 + str2
