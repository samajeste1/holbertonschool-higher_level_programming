#!/usr/bin/env python3
"""
Module for basic type annotations.
Provides a function to add two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum.

    Args:
        a: First float number
        b: Second float number

    Returns:
        The sum of a and b as a float
    """
    return a + b
